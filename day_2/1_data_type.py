#!/usr/bin/python3

# Subscripting
print("Hello"[0])

# Sring
print("123" + "456")

# Integer = whole number
print(123 + 345)

# Large integers
print(234_567_432)

# Float = floating point number
print(215.2)

# Boolean
print(True)
print(False)

# checking for data type
print(type(123))
print(type("Hello"))
print(type(21.3))
print(type(True))

# Data type conversion
num = 345
new_num = float(num)
print(new_num)

# Converted int to str and concatenated it
print("Number of letter in your name: " + str(len(input("What is your name "))))
